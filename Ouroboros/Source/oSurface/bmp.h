// Standard structs and definitions for the bitmap (.bmp) image file format.
#ifndef bmp_h
#define bmp_h

#include <stdint.h>

static const uint16_t bmp_signature = 0x4d42; // 'BM'

struct bmp_rgbquad
{
	uint8_t rgbBlue;
	uint8_t rgbGreen;
	uint8_t rgbRed;
	uint8_t rgbReserved;
};

struct bmp_infoheader
{
	uint32_t biSize;
	int32_t biWidth;
	int32_t biHeight;
	uint16_t biPlanes;
	uint16_t biBitCount;
	uint32_t biCompression;
	uint32_t biSizeImage;
	int32_t biXPelsPerMeter;
	int32_t biYPelsPerMeter;
	uint32_t biClrUsed;
	uint32_t biClrImportant;
};

struct bmp_info
{
	bmp_infoheader bmiHeader;
	bmp_rgbquad bmiColors[1];
};

#pragma pack(push,2)
struct bmp_header
{
	uint16_t bfType;
	uint32_t bfSize;
	uint16_t bfReserved1;
	uint16_t bfReserved2;
	uint32_t bfOffBits;
};
#pragma pack(pop)

#endif
